package com.stoneapp.ourvlemoodle2.models;

public class MoodleWarning {
	
	String item;
	int itemid;
	String warningcode;
	String message;
	public String getItem() {
		return item;
	}
	public int getItemid() {
		return itemid;
	}
	public String getWarningcode() {
		return warningcode;
	}
	public String getMessage() {
		return message;
	}

}
